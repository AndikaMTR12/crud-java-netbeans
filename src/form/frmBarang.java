    public void TampilData() {
        DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("KODE BARANG");
        DftTblModel_barang.addColumn("NAMA BARANG");
        DftTblModel_barang.addColumn("JENIS BARANG");
        DftTblModel_barang.addColumn("JUMLAH BARANG");
        tblBarang.setModel(DftTblModel_barang);
        Connection conn = Koneksi.getConnection();
        try { 
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from barang_masuk";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                    res.getString("kode_barang"),
                    res.getString("nama_barang"),
                    res.getString("jenis_barang"),
                    res.getString("jumlah_barang")
                });
            }
        } catch(SQLException e) {
            System.out.println(e.getMessage());
        }
    }
