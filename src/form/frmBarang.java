    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("insert into barang_masuk(kode_barang, nama_barang, jenis_barang, jumlah_barang) values(?,?,?,?)");
            stmt.setString(1, txtNoBarang.getText());
            stmt.setString(2, txtNamaBarang.getText());
            stmt.setString(3, txtJenisBarang.getText());
            stmt.setString(4, txtJumlahBarang.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            
            txtNoBarang.setText("");
            txtNamaBarang.setText("");
            txtJenisBarang.setText("");
            txtJumlahBarang.setText("");
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = Koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("update barang_masuk set nama_barang=?, jenis_barang=?, jumlah_barang=? where kode_barang=?");
            stmt.setString(1, txtNamaBarang.getText());
            stmt.setString(2, txtJenisBarang.getText());
            stmt.setString(3, txtJumlahBarang.getText());
            stmt.setString(4, txtNoBarang.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
            txtNoBarang.setText("");
            txtNamaBarang.setText("");
            txtJenisBarang.setText("");
            txtJumlahBarang.setText("");
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditMouseClicked
        // TODO add your handling code here:
        int baris = tblBarang.getSelectedRow();
        txtNoBarang.setText(DftTblModel_barang.getValueAt(baris, 0).toString());
        txtNamaBarang.setText(DftTblModel_barang.getValueAt(baris, 1).toString());
        txtJenisBarang.setText(DftTblModel_barang.getValueAt(baris, 2).toString());
        txtJumlahBarang.setText(DftTblModel_barang.getValueAt(baris, 3).toString());
    }//GEN-LAST:event_btnEditMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        Connection conn = Koneksi.getConnection();
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            try {
                java.sql.PreparedStatement stmt = conn.prepareStatement("delete from barang_masuk where kode_barang ='" + txtNoBarang.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                TampilData();
                txtNoBarang.setText("");
                txtNamaBarang.setText("");
                txtJenisBarang.setText("");
                txtJumlahBarang.setText("");
                txtNoBarang.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal di hapus" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

